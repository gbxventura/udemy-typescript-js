/*
Operadores de comparação
> - Maior que
>= - Maior que ou igual a 
< - menor que
<= - menor que ou igual a
== - igualdade (valor) 
=== - igualdade estrita (valor e tipo)
!= - diferente (valor)
!== - diferente estrito (valor e tipo)
*/

console.log(10 > 5); // true
console.log(10 >= 5); // true

let num1 = 10;
let num2 = 10;
const comp = num1 === num2;
console.log(comp);
